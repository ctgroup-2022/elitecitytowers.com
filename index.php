<script>
document.addEventListener('DOMContentLoaded', function() {
  // Elements
  const menuToggle = document.querySelector('.menu-toggle');
  const mobileMenu = document.querySelector('.mobile-menu');
  const toggleLines = document.querySelectorAll('.toggle-line');
  const navbar = document.querySelector('.minimal-navbar');

  // Mobile menu toggle
  let isMenuOpen = false;
  if (menuToggle && mobileMenu) {
    menuToggle.addEventListener('click', function() {
      isMenuOpen = !isMenuOpen;

      if (isMenuOpen) {
        mobileMenu.style.transform = 'translateY(0)';
        mobileMenu.style.opacity = '1';
        mobileMenu.style.visibility = 'visible';
        menuToggle.style.background = 'rgba(28, 140, 74, 0.1)';

        // Animate hamburger to X
        toggleLines[0].style.transform = 'rotate(45deg) translate(5px, 5px)';
        toggleLines[1].style.opacity = '0';
        toggleLines[2].style.transform = 'rotate(-45deg) translate(7px, -6px)';
      } else {
        mobileMenu.style.transform = 'translateY(-100%)';
        mobileMenu.style.opacity = '0';
        mobileMenu.style.visibility = 'hidden';
        menuToggle.style.background = 'none';

        // Reset hamburger
        toggleLines[0].style.transform = 'none';
        toggleLines[1].style.opacity = '1';
        toggleLines[2].style.transform = 'none';
      }
    });
  }

  // Active section tracking
  const sections = document.querySelectorAll('[id]');
  const navLinks = document.querySelectorAll('.menu-link, .mobile-nav-link');

  function setActiveLink() {
    const scrollPosition = window.scrollY + 100; // Offset for navbar height

    sections.forEach(section => {
      const sectionTop = section.offsetTop;
      const sectionHeight = section.offsetHeight;
      const sectionId = section.getAttribute('id');

      if (scrollPosition >= sectionTop && scrollPosition < sectionTop + sectionHeight) {
        // Remove active class from all links
        navLinks.forEach(link => {
          link.classList.remove('active');
          const indicator = link.querySelector('.link-indicator');
          if (indicator) {
            indicator.style.width = '0';
          }
        });

        // Add active class to current section link
        navLinks.forEach(link => {
          if (link.getAttribute('href') === `#${sectionId}`) {
            link.classList.add('active');
            const indicator = link.querySelector('.link-indicator');
            if (indicator) {
              indicator.style.width = '100%';
            }
            link.style.color = '#1C8C4A';
          }
        });
      }
    });
  }

  // Set initial active link
  setActiveLink();

  // Update active link on scroll
  window.addEventListener('scroll', setActiveLink);

  // Desktop menu hover effects (only for non-active links)
  const menuLinks = document.querySelectorAll('.menu-link');
  menuLinks.forEach(link => {
    const indicator = link.querySelector('.link-indicator');

    link.addEventListener('mouseenter', function() {
      if (!this.classList.contains('active')) {
        indicator.style.width = '100%';
        this.style.color = '#1C8C4A';
      }
    });

    link.addEventListener('mouseleave', function() {
      if (!this.classList.contains('active')) {
        indicator.style.width = '0';
        this.style.color = '#000000';
      }
    });
  });

  // Mobile menu link hover effects
  const mobileNavLinks = document.querySelectorAll('.mobile-nav-link');
  mobileNavLinks.forEach(link => {
    link.addEventListener('mouseenter', function() {
      if (!this.classList.contains('active')) {
        this.style.color = '#1C8C4A';
        this.style.transform = 'translateX(5px)';
      }
    });

    link.addEventListener('mouseleave', function() {
      if (!this.classList.contains('active')) {
        this.style.color = '#000000';
        this.style.transform = 'translateX(0)';
      }
    });
  });

  // Smooth scrolling for all links
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
      e.preventDefault();
      const target = document.querySelector(this.getAttribute('href'));
      if (target) {
        const headerOffset = 80;
        const elementPosition = target.offsetTop;
        const offsetPosition = elementPosition - headerOffset;

        // Close mobile menu if open
        if (isMenuOpen) {
          mobileMenu.style.transform = 'translateY(-100%)';
          mobileMenu.style.opacity = '0';
          mobileMenu.style.visibility = 'hidden';
          menuToggle.style.background = 'none';
          toggleLines[0].style.transform = 'none';
          toggleLines[1].style.opacity = '1';
          toggleLines[2].style.transform = 'none';
          isMenuOpen = false;
        }

        window.scrollTo({
          top: offsetPosition,
          behavior: 'smooth'
        });
      }
    });
  });

  // Navbar scroll effect - Sticky white navbar
  window.addEventListener('scroll', function() {
    if (navbar) {
      if (window.scrollY > 30) {
        navbar.style.background = '#ffffff';
        navbar.style.boxShadow = '0 2px 20px rgba(0,0,0,0.3)';
        navbar.style.borderBottom = '2px solid #F58220';
      } else {
        navbar.style.background = '#ffffff';
        navbar.style.boxShadow = '0 2px 10px rgba(0,0,0,0.3)';
        navbar.style.borderBottom = '2px solid #F58220';
      }
    }
  });

  // CTA button hover effects
  const ctaButtons = document.querySelectorAll('.cta-btn, .mobile-cta-btn');
  ctaButtons.forEach(button => {
    button.addEventListener('mouseenter', function() {
      this.style.transform = 'translateY(-1px)';
      this.style.boxShadow = '0 4px 15px rgba(28, 140, 74, 0.4)';
    });

    button.addEventListener('mouseleave', function() {
      this.style.transform = 'translateY(0)';
      this.style.boxShadow = '0 2px 10px rgba(28, 140, 74, 0.3)';
    });
  });
});
